def save_picture(profile_pic):
    random_hex = secrets.token_hex(8)
    _, f_ext = os.path.splitext(profile_pic.filename)
    picture_fn = random_hex + f_ext
    picture_path = os.path.join(current_app.config['UPLOAD_FOLDER'], picture_fn)
    print(picture_fn)
    # Save the picture
    profile_pic.save(picture_path)
    print(picture_path)
    return picture_fn

# This endpoint handles the actual upload process
@bp.route('/upload', methods=['POST'])
@login_required
def handle_upload():
    if 'file' not in request.files:
        flash('No file part')
        return redirect(request.url)
    file = request.files['file']
    if file.filename == '':
        flash('No selected file')
        return redirect(request.url)
    if file and allowed_file(file.filename):
        filename = secure_filename(file.filename)
        file_path = os.path.join(current_app.config['UPLOAD_FOLDER'], filename)
        file.save(file_path)
        current_user.profile_pic = filename
        print(filename)
        db.session.commit()
        flash('Your profile image has been updated!')
        return redirect(url_for('main.user', username=current_user.username))
    else:
        flash('File type is not allowed.')
        return redirect(request.url)

